public class TrackInstructor {

    int instructorID;
    String instructorName;
    String instructorPosition;
    String instructorImage;

    public int getInstructorID() {
        return instructorID;
    }

    public void setInstructorID(int instructorID) {
        this.instructorID = instructorID;
    }

    public String getInstructorName() {
        return instructorName;
    }

    public void setInstructorName(String instructorName) {
        this.instructorName = instructorName;
    }

    public String getInstructorPosition() {
        return instructorPosition;
    }

    public void setInstructorPosition(String instructorPosition) {
        this.instructorPosition = instructorPosition;
    }

    public String getInstructorImage() {
        return instructorImage;
    }

    public void setInstructorImage(String instructorImage) {
        this.instructorImage = instructorImage;
    }
}
